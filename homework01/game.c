void main_control() //메인 시스템 함수. 나중에 메인 함수로 옮길수도 있음
{
	int select;
	while (goto_main != 0)
	{
		draw_title();
		gotoxy(24, 22);
		printf("1. 게임 시작\n");
		gotoxy(24, 23);
		printf("2. 게임 설명\n");
		gotoxy(24, 24);
		printf("3. 게임 종료\n");
		gotoxy(17, 26);
		printf("1~3 숫자를 입력하고 엔터 > ");
		scanf("%d", &select);
		if (select == 1)
		{
			game_system();
			ending();
			goto_main = 1;
		}
		else if (select == 2)
		{
			print_how_to_play();
			goto_main = 1;
		}
		else if (select == 3)
		{
			print_game_quit();
			goto_main = 0;
		}
		else
		{
			gotoxy(17, 27);
			printf("유효한 숫자를 입력하세요.\n");
			Sleep(3000);
			goto_main = 1;
		}
	}
}

void game_system() //게임 플레이 함수
{
	int toending, choice = 0; //toending : 진행도 숫자 | choice : 선택한 활동 받아오기
	physical = 0, intelli = 0, mental = 0, death = 0;
	for (toending = 1; toending <= 13; toending += 1) //총 13번 반복함.
	{
		draw_gamescreen();
		printf("진행도 : %d/13", toending-1);
		gotoxy(17, 26);
		printf("1~3 숫자를 입력하고 엔터 > ");
		scanf("%d", &choice);
		switch (choice)
		{
			case 1:
			{
				draw_intelli();
				intelli += 10;
			}
			break;
			case 2:
			{
				draw_mental();
				mental += 10;
			}
			break;
			case 3:
			{
				draw_physical();
				physical += 10;
			}
			break;
			default:
			{
				death += 1;
				draw_death();
			}
		}
	}

}

void ending() //엔딩 부분 함수
{
	if (intelli > physical && intelli > mental && intelli > death)
	{
		print_intelli_ending();
	}
	else if (mental > intelli && mental > physical && mental > death)
	{
		print_mental_ending();
	}
	else if (physical > intelli && physical > mental && physical > death)
	{
		print_physical_ending();
	}
	else if (intelli == 0 && mental == 0 && physical == 0)
	{
		print_death_ending();
	}
}



